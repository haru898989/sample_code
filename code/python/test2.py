"""
=========================================================
AI物体検知 基礎学習辞書 (YOLO-World編)

※このコードは「YOLO-World」を使って、画像の中から
 指定した英単語の物体を探し出すための教材です。
=========================================================
"""

from ultralytics import YOLO
import cv2

def main():
    # ---------------------------------------------------------
    # 1. モデルの読み込みとサイズ選択
    # ---------------------------------------------------------
    # 'yolov8s-world.pt' の 's' は Small（スモール）を意味します。
    # YOLO-Worldモデルをインターネット経由で自動ダウンロードして読み込みます。
    model = YOLO('yolov8s-world.pt')

    # ---------------------------------------------------------
    # 2. オープンボキャブラリー（YOLO-World最大の強み）
    # ---------------------------------------------------------
    # 従来のYOLOは「学習済みの決まった物体（犬、車など80種類）」しか探せませんでしたが、
    # YOLO-Worldは「テキスト（英単語）で指示した未知の物体」を探すことができます。
    custom_classes = [
        "roller coaster", 
        "amusement park ride", 
        "guitar", 
        "sky", 
        "drum", 
        "microphone"
    ]
    model.set_classes(custom_classes)
    print(f"探す物体をセットしました: {custom_classes}")

    # ---------------------------------------------------------
    # 3. 推論（AIによる予測）の実行
    # ---------------------------------------------------------
    # conf=0.1 は「確信度（自信）が10%以上あるものだけを結果に残す」という設定です。
    # 低くするとたくさん見つけますが、間違い（誤検知）も増えます。
    results = model('sample.jpg', conf=0.1)

    # ---------------------------------------------------------
    # 4. 結果の解析と取り出し
    # ---------------------------------------------------------
    # 画像1枚ごとの結果が results に入っています（今回は1枚のみ）
    for result in results:
        # 見つかった物体（バウンディングボックス）を1つずつ確認
        for box in result.boxes:
            # 予測した物体のID（配列の何番目か）
            class_id = int(box.cls[0])
            # IDを文字列（名前）に変換
            class_name = model.names[class_id]
            
            # AIの確信度（0.0 ～ 1.0）
            confidence = float(box.conf[0])
            
            # 結果をコンソールに表示
            print(f"見つけたもの: {class_name} (自信: {confidence:.2f})")

        # ---------------------------------------------------------
        # 5. 結果の可視化（OpenCV）
        # ---------------------------------------------------------
        # AIが四角い枠（バウンディングボックス）を描き込んだ画像を生成
        annotated_img = result.plot() 
        
        # ウィンドウに表示
        cv2.imshow("YOLO-World Result", annotated_img)
        cv2.waitKey(0) # キーボードのキーが押されるまで待機
        cv2.destroyAllWindows() # ウィンドウを閉じる

if __name__ == "__main__":
    main()